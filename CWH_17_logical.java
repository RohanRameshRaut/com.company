public class CWH_17_logical {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        if(a && b)
        {
            System.out.println("Y");
        }
        else
        {
            System.out.println("N");
        }

        System.out.println("For logical NOT");
        System.out.println(!a);
        System.out.println(!b);
    }
}
