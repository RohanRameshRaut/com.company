public class CWH_21_Ch5_loops {
    public static void main(String[] args) {

        // While loop
        int i = 1;
        while(i<=3) // while(boolean expression)--> while loop, evaluates on boolean expression(condition) in java language.
        {
            System.out.println(i);
            i++;
        }
        System.out.println("While loop end.");
    }
}
