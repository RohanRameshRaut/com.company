package com.company;

public class CWH_62_Ch12_Packages {
    public static void main(String[] args) {
        /*
        * Interpreter VS Compiler
        * Interpreter translates one statement at a time into machine code.
        * Compiler Scans the entire program and translates whole of it into machine code.
        * Interpreter:-
        * One statement at a time
        * Interpreter is needed everytime to run the program like python is an interpreted language, you can't run the program by deleting the interpreter.
        * Partial execution if error, means if you write a code of 100 lines and you get an error at line no 70 then Interpreter will run the code of all 1 to 69 line by showing the error at line no 70 as it reads the code line by line and runs it line by line also.
        * Usually easy for programmers
        * ex: Python, JavaScript
        * Slower than Compile languages
        *
        * Compiler:-
        * Entire program at a time
        * Once compiled it is not needed, means if you run the c language code compiler(i.e GCC) will create .exe file(machine code), after that you can run the .exe file even if you delete the compiler.
        * No execution if an error occurs as it reads the whole code and then runs it.
        * Usually not as easy as Interpreted ones
        * ex: C, C++
        * Faster than Interpreted languages
        *
        * */
    }
}
